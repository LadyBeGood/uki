    proc exprToString*(expr: Expression, indentLevel: int = 0): string =
        let indent = "    ".repeat(indentLevel)
        
        if expr of ContainerExpression:
            let e = ContainerExpression(expr)
            result.add indent & "ContainerExpression: " & e.identifier.lexeme & "\n"
            if e.arguments.len > 0:
                result.add indent & "    Arguments:\n"
                for arg in e.arguments:
                    result.add exprToString(arg, indentLevel + 2)
    
        elif expr of WhenExpression:
            let e = WhenExpression(expr)
            result.add indent & "WhenExpression:\n"
            for subexpr in e.whenSubExpressions:
                result.add indent & "    WhenSubExpression:\n"
                result.add indent & "        Keyword: " & subexpr.keyword.lexeme & "\n"
                if subexpr.condition != nil:
                    result.add indent & "        Condition:\n"
                    result.add exprToString(subexpr.condition, indentLevel + 3)
                result.add indent & "        Expression:\n"
                result.add exprToString(subexpr.expression, indentLevel + 3)
    
        elif expr of LoopExpression:
            let e = LoopExpression(expr)
            result.add indent & "LoopExpression: " & e.loopKeyword.lexeme & "\n"
            for withExpr in e.withExpressions:
                result.add indent & "    WithExpression:\n"
                result.add indent & "        Iterable:\n"
                result.add exprToString(withExpr.iterable, indentLevel + 3)
                if withExpr.counters.len > 0:
                    result.add indent & "        Counters:\n"
                    for counter in withExpr.counters:
                        result.add indent & "           " & counter.lexeme & "\n"
            result.add indent & "    Expression:\n"
            result.add exprToString(e.expression, indentLevel + 2)
    
        elif expr of TryExpression:
            let e = TryExpression(expr)
            result.add indent & "TryExpression: " & e.tryKeyword.lexeme & "\n"
            result.add indent & "   TryExpression:\n"
            result.add exprToString(e.tryExpression, indentLevel + 2)
            for fixExpr in e.fixExpressions:
                result.add indent & "   FixExpression:\n"
                result.add indent & "       Keyword: " & fixExpr.fixKeyword.lexeme & "\n"
                if fixExpr.identifier != nil:
                    result.add indent & "       Identifier: " & fixExpr.identifier.lexeme & "\n"
                result.add indent & "       Expression:\n"
                result.add exprToString(fixExpr.fixExpression, indentLevel + 3)
    
        elif expr of BlockExpression:
            let e = BlockExpression(expr)
            result.add indent & "BlockExpression:\n"
            result.add astToString(e.statements, indentLevel + 1)
    
        elif expr of LiteralExpression:
            let e = LiteralExpression(expr)
            if e.value of BooleanLiteral:
                let val = BooleanLiteral(e.value)
                result.add indent & "BooleanLiteral: " & $val.value & "\n"
            elif e.value of StringLiteral:
                let val = StringLiteral(e.value)
                result.add indent & "StringLiteral: " & val.value & "\n"
            elif e.value of NumericLiteral:
                let val = NumericLiteral(e.value)
                result.add indent & "NumericLiteral: " & $val.value & "\n"
    
        elif expr of GroupingExpression:
            let e = GroupingExpression(expr)
            result.add indent & "GroupingExpression:\n"
            result.add exprToString(e.expression, indentLevel + 1)
    
        elif expr of UnaryExpression:
            let e = UnaryExpression(expr)
            result.add indent & "UnaryExpression: " & e.operator.lexeme & "\n"
            result.add exprToString(e.right, indentLevel + 1)
    
        elif expr of RangeExpression:
            let e = RangeExpression(expr)
            result.add indent & "RangeExpression:\n"
            result.add indent & "   Start:\n"
            result.add exprToString(e.start, indentLevel + 2)
            result.add indent & "   First Operator: " & e.firstOperator.lexeme & "\n"
            result.add indent & "   Stop:\n"
            result.add exprToString(e.stop, indentLevel + 2)
            if e.secondOperator != nil:
                result.add indent & "   Second Operator: " & e.secondOperator.lexeme & "\n"
                result.add indent & "   Step:\n"
                result.add exprToString(e.step, indentLevel + 2)
    
        elif expr of BinaryExpression:
            let e = BinaryExpression(expr)
            result.add indent & "BinaryExpression: " & e.operator.lexeme & "\n"
            result.add indent & "   Left:\n"
            result.add exprToString(e.left, indentLevel + 2)
            result.add indent & "   Right:\n"
            result.add exprToString(e.right, indentLevel + 2)
    
    proc astToString*(statements: seq[Statement], indentLevel: int = 0): string =
        let indent = "  ".repeat(indentLevel)
        
        for stmt in statements:
            if stmt of ExpressionStatement:
                let s = ExpressionStatement(stmt)
                result.add indent & "ExpressionStatement:\n"
                result.add exprToString(s.expression, indentLevel + 1)
                
            elif stmt of ContainerStatement:
                let s = ContainerStatement(stmt)
                result.add indent & "ContainerStatement: " & s.identifier.lexeme & "\n"
                if s.parameters.len > 0:
                    result.add indent & "   Parameters:\n"
                    for param in s.parameters:
                        result.add indent & "                " & param.lexeme & "\n"
                result.add indent & "   Expression:\n"
                result.add exprToString(s.expression, indentLevel + 2)
                
            elif stmt of WhenStatement:
                let s = WhenStatement(stmt)
                result.add indent & "WhenStatement:\n"
                for substmt in s.whenSubStatements:
                    result.add indent & "   WhenSubStatement:\n"
                    result.add indent & "       Keyword: " & substmt.keyword.lexeme & "\n"
                    if substmt.condition != nil:
                        result.add indent & "       Condition:\n"
                        result.add exprToString(substmt.condition, indentLevel + 3)
                    result.add indent & "       Block:\n"
                    result.add exprToString(substmt.block, indentLevel + 3)
                    
            elif stmt of LoopStatement:
                let s = LoopStatement(stmt)
                result.add indent & "LoopStatement: " & s.loopKeyword.lexeme & "\n"
                for withStmt in s.withStatements:
                    result.add indent & "   WithStatement:\n"
                    result.add indent & "       Iterable:\n"
                    result.add exprToString(withStmt.iterable, indentLevel + 3)
                    if withStmt.counters.len > 0:
                        result.add indent & "       Counters:\n"
                        for counter in withStmt.counters:
                            result.add indent & "           " & counter.lexeme & "\n"
                result.add indent & "   Block:\n"
                result.add exprToString(s.block, indentLevel + 2)
                
            elif stmt of TryStatement:
                let s = TryStatement(stmt)
                result.add indent & "TryStatement:\n"
                result.add indent & "   TryBlock:\n"
                result.add exprToString(s.tryBlock, indentLevel + 2)
                for fixStmt in s.fixStatements:
                    result.add indent & "   FixStatement:\n"
                    result.add indent & "       Keyword: " & fixStmt.fixKeyword.lexeme & "\n"
                    if fixStmt.identifier != nil:
                        result.add indent & "       Identifier: " & fixStmt.identifier.lexeme & "\n"
                    result.add indent & "       Block:\n"
                    result.add exprToString(fixStmt.fixBlock, indentLevel + 3)
                    
            elif stmt of ExitStatement:
                let s = ExitStatement(stmt)
                result.add indent & "ExitStatement: " & s.keyword.lexeme & "\n"
                if s.expression != nil:
                    result.add exprToString(s.expression, indentLevel + 1)
                    
            elif stmt of StopStatement:
                let s = StopStatement(stmt)
                result.add indent & "StopStatement: " & s.keyword.lexeme & "\n"
                if s.counter != nil:
                    result.add indent & "   Counter: " & s.counter.lexeme & "\n"
                    
            elif stmt of NextStatement:
                let s = NextStatement(stmt)
                result.add indent & "NextStatement: " & s.keyword.lexeme & "\n"
                if s.counter != nil:
                    result.add indent & "   Counter: " & s.counter.lexeme & "\n"
    
